void main() {
  List<int> numbers = [1, 2, 3, 4, 5];
  for (int number in numbers) {
    print(number);
  }
  // output:
  // 1
  // 2
  // 3
  // 4
  // 5

  List<String> fruits = ['apple', 'banana', 'orange'];
  for (String fruit in fruits) {
    print(fruit);
  }
  // output:
  // apple
  // banana
  // orange

}